    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "cell", for: indexPath)

        // Configure the cell...
        
        cell.textLabel?.text = String(numberArr[indexPath.row])

        return cell
    }
    
    
    override func prepare(for segue: UIStoryboardSegue, sender: Any?) {
        if let detailViewController = segue.destination as? ViewController {
            if let indexPath = tableView.indexPathForSelectedRow {
                detailViewController.data = String(numberArr[indexPath.row])
                detailViewController.passData = { [weak self] data in
                    self?.numberArr[indexPath.row] = Int(data) ?? 0
                }
            }
            else {
                detailViewController.passData = { [weak self] data in
                    self?.numberArr.append(Int(data) ?? 0)
                }
            }
        }
    }
    

